function add(num1 ,num2){
    // return num1 + num2;
    const sum = num1 + num2;
    return sum;
}
function sub(num1, num2){
    // return num1 - num2;
    const sub = num1 - num2;
    return sub;
}
function multi(num1, num2){
    // return num1 * num2;
    const multi = num1 * num2;
    return multi;
}
function div(num1, num2){
    // return num1 / num2;
    const div = num1 / num2;
    return div;
}       

const result1 = add(10 , 5);
const result2 = sub(10 , 5);
const result3 = multi(10 , 5);
const result4 = div(10 , 5);

console.log( result1 , result2 , result3 , result4);